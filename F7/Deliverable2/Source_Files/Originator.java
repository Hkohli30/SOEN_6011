/**
 * Originator Interface for Memento Pattern
 * Interface for storage of values of a,b,x
 */

public interface Originator {
	
	/**
	 * This method is a skeleton method for getting value of A
	 */
	public double getA();


	/**
	 * This method is a skeleton method for getting value of B
	 */
	public double getB();


	/**
	 * This method is a skeleton method for getting value of X
	 */
	public double getX();

}
